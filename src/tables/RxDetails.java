package tables;

public class RxDetails {
	
	private int idRxDetails, days;
	private String mname, dose;

	public int getIdRxDetails() {
		return idRxDetails;
	}

	public void setIdRxDetails(int idRxDetails) {
		this.idRxDetails = idRxDetails;
	}
	
	public int getDays() {
		return days;
	}

	public void setDays(int days) {
		this.days = days;
	}

	public String getMname() {
		return mname;
	}

	public void setMname(String mname) {
		this.mname = mname;
	}

	public String getDose() {
		return dose;
	}

	public void setDose(String dose) {
		this.dose = dose;
	}
	
}
